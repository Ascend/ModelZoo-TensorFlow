import numpy as np
from tqdm import tqdm


def makeData(n = 50000):
    
    for i in tqdm(range(n // 100)):
        
        temp = np.random.randint(10, size=(100),)
        temp2 = np.random.normal(size=(100,128)).astype(np.float32)
        temp.tofile("./bindata_32/data0/{}.bin".format(i))
        temp2.tofile("./bindata_32/data1/{}.bin".format(i))
makeData()

